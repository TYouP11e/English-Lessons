      const levels = ["A1", "A2", "B1", "B2", "C1", "C2"];
      let currentLevelIndex = 0;

      const levelsContainer = document.getElementById("levelsContainer");
      const resultDiv = document.getElementById("result");

      function renderLevel(level) {
        const levelDiv = document.createElement("div");
        levelDiv.className = "levelss";
        levelDiv.id = `level-${level}`;
        levelDiv.style.display = "block";


const header = document.createElement("h2");
        header.textContent = `Level ${level}`;
        levelDiv.appendChild(header);

        const questionsForLevel = questions.filter((q) => q.level === level);

        questionsForLevel.forEach((q, index) => {
          const questionDiv = document.createElement("div");
          questionDiv.className = "question";

          const questionText = document.createElement("p");
          questionText.textContent = q.question;
          questionDiv.appendChild(questionText);

          q.options.forEach((opt) => {
            const label = document.createElement("label");
            const input = document.createElement("input");
            input.type = "radio";
            input.name = `${level}-q${index}`;
            input.value = opt;
            label.appendChild(input);
            label.append(` ${opt}`);
            questionDiv.appendChild(label);
            questionDiv.appendChild(document.createElement("br"));
          });

          levelDiv.appendChild(questionDiv);
        });

        const submitBtn = document.createElement("button");
        submitBtn.textContent = "Submit";
        submitBtn.classList.add("submitresultbtn");
        submitBtn.addEventListener("click", () =>
          checkLevel(level, questionsForLevel)
        );
        levelDiv.appendChild(submitBtn);

        levelsContainer.appendChild(levelDiv);
      }

